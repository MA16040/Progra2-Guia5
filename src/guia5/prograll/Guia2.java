    private void btnIngresarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarDatosActionPerformed
        if (txtIngresar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un dato antes de presionar");
        } else {
            txtIngresar.requestFocus();
            if (cont < 10) {
                txtIngresar.setEnabled(true);
                cont = cont + 1;
                dato3 = txtIngresar.getText().toUpperCase();
                Vector[(cont - 1)] = dato3;
                txtIngresar.setText("");
                if (cont < 10) {
                    lblDato.setText("Dato " + (cont + 1));
                }
                if (cont==10) {
                txtIngresar.setEnabled(false);
                txtIngresar.setText("");
                btnIngresarDatos.setEnabled(false);
                btnMostrar.setEnabled(true);
                btnOrdenar.setEnabled(true);
            }
            } 

            
        }

    }//GEN-LAST:event_btnIngresarDatosActionPerformed

    private void txtIngresarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIngresarKeyTyped
        char dato = evt.getKeyChar();
        String tamanio = txtIngresar.getText();

        if ((dato < 65 || dato > 90) && (dato < 97 || dato > 122) || (tamanio.length() >= 1)) {
            evt.consume();

        }


    }//GEN-LAST:event_txtIngresarKeyTyped

    private void txtIngresarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIngresarKeyPressed
        if (evt.getKeyCode() == 10) {
            btnIngresarDatosActionPerformed(null);
        }

    }//GEN-LAST:event_txtIngresarKeyPressed
