    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
       txtAreaMostrar.setText("");
       Impresion="";
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {

                dato1 = Vector[j].charAt(0);
                dato2 = Vector[j + 1].charAt(0);
                if (dato1 > dato2) {
                    burbuja = Vector[j];
                    Vector[j] = Vector[j + 1];
                    Vector[j + 1] = burbuja;
                }
            }
        }
        JOptionPane.showMessageDialog(null, "Ordenado con Exito");

    }//GEN-LAST:event_btnOrdenarActionPerformed
